#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	if ((!s1 && !s2) || n == 0)
		return (1);
	else if (!s1 || !s2)
		return (0);
	i = 0;
	while (s1[i] == s2[i] && s1[i] != 0 && s2[i] != 0 && i < (n - 1))
		i++;
	return (s1[i] == s2[i]);
}
