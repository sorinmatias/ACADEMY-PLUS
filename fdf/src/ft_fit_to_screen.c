#include "fdf.h"

int		ft_fit_to_screen(t_env *m)
{
	int i;
	int j;

	i = -1;
	while (++i < m->row)
	{
		j = -1;
		while (++j < m->col)
		{
			if (m->map_i[i][j].x < 0 || m->map_i[i][j].y < 0)
			{
				m->tile_height -= 0.5;
				return (1);
			}
		}
	}
	return (0);
}
