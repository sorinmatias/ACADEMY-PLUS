#include "ft_printf.h"

wchar_t		*ft_wmemset(wchar_t *wstr, wchar_t c, size_t len)
{
	size_t i;

	i = 0;
	while (i < len)
	{
		wstr[i] = c;
		i++;
	}
	return (wstr);
}